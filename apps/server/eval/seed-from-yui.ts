import process from "node:process";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import pg from "pg";

const { Pool } = pg;

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = join(HERE, "fixtures-private");

interface YuiMemory {
  id: string;
  narrative: string;
  importance: number;
  kind: string;
  created_at: Date;
}

interface YuiObservation {
  id: string;
  content: string;
  type: string;
  source: string;
  created_at: Date;
}

interface YuiDecision {
  id: string;
  content: string;
  status: string;
  created_at: Date;
}

interface YuiLink {
  from_id: string;
  to_id: string;
  relation: string;
}

function q(value: unknown): string {
  return JSON.stringify(value);
}

function header(typeImport: string): string {
  return [
    "import type { FixtureCase } from \"../types.js\";",
    typeImport,
    "",
    "// Generated by eval/seed-from-yui.ts from yui operational data.",
    "// Private fixtures are gitignored. Fill expected labels by human review.",
    "",
  ].join("\n");
}

function sourceOf(source: string): "claude-code" | "codex" | "yui" | "other" {
  if (source === "claude-code" || source === "codex" || source === "yui") {
    return source;
  }
  return "other";
}

function typeOf(
  type: string,
): "discovery" | "progress" | "blocker" | "decision" | "reflection" | "other" {
  if (
    type === "discovery" ||
    type === "progress" ||
    type === "blocker" ||
    type === "decision" ||
    type === "reflection"
  ) {
    return type;
  }
  return "other";
}

function renderAudn(observations: YuiObservation[]): string {
  const body = observations
    .slice(0, 120)
    .map((o, i) =>
      [
        "  {",
        `    id: "private-audn-${String(i + 1).padStart(3, "0")}",`,
        `    description: ${q(`yui observation ${o.id} (${o.created_at.toISOString()})`)},`,
        "    input: {",
        `      newFact: ${q(o.content)},`,
        "      existingMemoryNarratives: [],",
        "    },",
        "    // TODO: label by human",
        "    expected: { decision: \"ADD\", targetIndex: null },",
        "  },",
      ].join("\n"),
    )
    .join("\n");

  return (
    header(
      "import type { AudnExpected, AudnInput } from \"../fixtures/audn.synthetic.js\";",
    ) + `export const fixtures: FixtureCase<AudnInput, AudnExpected>[] = [\n${body}\n];\n`
  );
}

