function renderSearch(memories: YuiMemory[], observations: YuiObservation[]): string {
  const seed = memories
    .slice(0, 120)
    .map((m, i) =>
      [
        "  {",
        `    id: "mem_evalseed_private_${String(i + 1).padStart(3, "0")}",`,
        `    narrative: ${q(m.narrative)},`,
        `    importance: ${Number(m.importance).toFixed(2)},`,
        `    kind: ${q(m.kind)},`,
        "  },",
      ].join("\n"),
    )
    .join("\n");

  const fixtures = observations
    .slice(0, 120)
    .map((o, i) =>
      [
        "  {",
        `    id: "private-search-${String(i + 1).padStart(3, "0")}",`,
        `    description: ${q(`query from observation ${o.id}`)},`,
        "    input: {",
        `      query: ${q(o.content)},`,
        "      limit: 10,",
        "    },",
        "    // TODO: label by human",
        "    expected: { expectedIds: [] },",
        "    tags: [\"ambiguous\"],",
        "  },",
      ].join("\n"),
    )
    .join("\n");

  return (
    header(
      "import type { SearchExpected, SearchInput } from \"../fixtures/search.synthetic.js\";",
    ) +
    `export const seedMemories = [\n${seed}\n];\n\n` +
    `export const fixtures: FixtureCase<SearchInput, SearchExpected>[] = [\n${fixtures}\n];\n`
  );
}

function renderContradiction(decisions: YuiDecision[]): string {
  const chunks: YuiDecision[][] = [];
  for (let i = 0; i < decisions.length; i += 8) {
    chunks.push(decisions.slice(i, i + 8));
  }
  const body = chunks
    .slice(0, 20)
    .map((chunk, i) =>
      [
        "  {",
        `    id: "private-contradiction-${String(i + 1).padStart(3, "0")}",`,
        `    description: ${q(`decision batch ${i + 1}`)},`,
        "    input: {",
        "      decisions: [",
        ...chunk.map(
          (d) =>
            `        { isoDate: ${q(d.created_at.toISOString().slice(0, 10))}, content: ${q(d.content)} },`,
        ),
        "      ],",
        "    },",
        "    // TODO: label by human",
        "    expected: { pairs: [] },",
        "  },",
      ].join("\n"),
    )
    .join("\n");

  return (
    header(
      "import type { ContradictionExpected, ContradictionInput } from \"../fixtures/contradiction.synthetic.js\";",
    ) +
    `export const fixtures: FixtureCase<ContradictionInput, ContradictionExpected>[] = [\n${body}\n];\n`
  );
}

