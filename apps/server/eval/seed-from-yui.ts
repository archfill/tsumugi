/**
 * Extract private evaluation fixtures from yui's production database.
 *
 * Output (under eval/fixtures-private/, gitignored):
 *   - promote.private.ts       : auto-labeled (linked memory → keep, else skip)
 *   - audn.private.ts          : auto-labeled UPDATE/DELETE from memory_history
 *   - search.private.ts        : auto-labeled self-recall (memory narrative → its own id)
 *   - time-update.private.ts   : TODO-labeled (human review needed)
 *
 * contradiction.private.ts is NOT generated — yui has 0 superseded_by relations.
 *
 * Run with:
 *   YUI_DATABASE_URL=postgresql://... mise run -C apps/server eval-seed
 */

import process from "node:process";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import pg from "pg";

const { Pool } = pg;

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = join(HERE, "fixtures-private");

// ---------------------------------------------------------------------------
// Row types (yui schema)
// ---------------------------------------------------------------------------

interface YuiMemoryRow {
  id: string;
  narrative: string;
  importance: number;
  kind: string;
  created_at: Date;
}

interface YuiPromoteRow {
  obs_id: string;
  source: string;
  kind: string;
  narrative: string;
  recorded_at: Date;
  linked: boolean;
}

interface YuiAudnRow {
  hist_id: string;
  memory_id: string;
  action: "UPDATE" | "DELETE";
  before_narrative: string;
  after_narrative: string | null;
  reason: string | null;
  applied_at: Date;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

const RELATIVE_TIME_RE =
  /\b(yesterday|today|tomorrow|days? ago|months? ago|weeks? ago|years? ago|recently|now|currently|just now)\b|(今日|明日|昨日|現在|今|最近|先週|先月|今週|今月|数日前|数か?月前|数年前|つい先ほど|今しがた)/u;

function q(value: unknown): string {
  return JSON.stringify(value);
}

function header(typeImport: string): string {
  return [
    'import type { FixtureCase } from "../types.js";',
    typeImport,
    "",
    "// Generated by eval/seed-from-yui.ts from yui operational data.",
    "// Private fixtures are gitignored. Edit at your own risk.",
    "",
  ].join("\n");
}

function sourceOf(
  yuiSource: string,
): "claude-code" | "codex" | "yui" | "other" {
  if (yuiSource === "claude_code" || yuiSource === "claude-code")
    return "claude-code";
  if (yuiSource === "codex") return "codex";
  if (yuiSource === "yui") return "yui";
  return "other";
}

function kindToType(
  yuiKind: string,
): "discovery" | "progress" | "blocker" | "decision" | "reflection" | "other" {
  if (
    yuiKind === "discovery" ||
    yuiKind === "progress" ||
    yuiKind === "blocker" ||
    yuiKind === "decision" ||
    yuiKind === "reflection"
  )
    return yuiKind;
  // Map yui-specific kinds to closest tsumugi type.
  if (yuiKind === "episodic" || yuiKind === "semantic") return "discovery";
  if (yuiKind === "tool_use") return "progress";
  if (yuiKind === "session" || yuiKind === "summary") return "reflection";
  return "other";
}

// ---------------------------------------------------------------------------
// Fixture renderers
// ---------------------------------------------------------------------------

function renderPromote(rows: YuiPromoteRow[]): string {
  const body = rows
    .map((r, i) => {
      const tag = r.linked ? "linked → keep" : "unlinked → skip";
      return [
        "  {",
        `    id: "private-promote-${String(i + 1).padStart(3, "0")}",`,
        `    description: ${q(`yui obs ${r.obs_id.slice(0, 8)} (${tag})`)},`,
        "    input: {",
        `      source: ${q(sourceOf(r.source))},`,
        `      type: ${q(kindToType(r.kind))},`,
        `      content: ${q(r.narrative)},`,
        "    },",
        `    expected: { skip: ${!r.linked} },`,
        "  },",
      ].join("\n");
    })
    .join("\n");

  return (
    header(
      'import type { PromoteExpected, PromoteInput } from "../fixtures/promote.synthetic.js";',
    ) +
    `export const fixtures: FixtureCase<PromoteInput, PromoteExpected>[] = [\n${body}\n];\n`
  );
}

function renderAudn(rows: YuiAudnRow[]): string {
  const items: string[] = [];
  for (const [i, r] of rows.entries()) {
    const newFact =
      r.action === "UPDATE"
        ? r.after_narrative || r.reason
        : r.reason || r.after_narrative;
    if (!newFact || !r.before_narrative) continue;
    items.push(
      [
        "  {",
        `    id: "private-audn-${r.action.toLowerCase()}-${String(i + 1).padStart(3, "0")}",`,
        `    description: ${q(`yui memory_history ${r.hist_id.slice(0, 8)} (${r.action})`)},`,
        "    input: {",
        `      newFact: ${q(newFact)},`,
        `      existingMemoryNarratives: [${q(r.before_narrative)}],`,
        "    },",
        `    expected: { decision: ${q(r.action)}, targetIndex: 0 },`,
        "  },",
      ].join("\n"),
    );
  }
  return (
    header(
      'import type { AudnExpected, AudnInput } from "../fixtures/audn.synthetic.js";',
    ) +
    `export const fixtures: FixtureCase<AudnInput, AudnExpected>[] = [\n${items.join("\n")}\n];\n`
  );
}

function renderSearch(memories: YuiMemoryRow[]): string {
  const SEED_TAKE = 100;
  const QUERY_TAKE = 80;

  const seeds = memories.slice(0, SEED_TAKE).map((m, idx) => ({
    id: `mem_evalseed_yui_${String(idx + 1).padStart(3, "0")}`,
    src: m,
  }));

  const seedBody = seeds
    .map((s) =>
      [
        "  {",
        `    id: ${q(s.id)},`,
        `    narrative: ${q(s.src.narrative)},`,
        `    importance: ${Number(s.src.importance).toFixed(2)},`,
        `    kind: ${q(s.src.kind)},`,
        "  },",
      ].join("\n"),
    )
    .join("\n");

  // Self-recall: take the first 80 seeds and use a distinctive slice of their
  // narrative as the query. Expect that seed's id in the top-k result.
  const queryBody = seeds
    .slice(0, QUERY_TAKE)
    .map((s, i) => {
      const narrative = s.src.narrative;
      // Use a 60-char slice from offset 0; if too short, use full narrative.
      const queryText =
        narrative.length > 60 ? narrative.slice(0, 60) : narrative;
      return [
        "  {",
        `    id: "private-search-${String(i + 1).padStart(3, "0")}",`,
        `    description: ${q(`self-recall ${s.id}`)},`,
        "    input: {",
        `      query: ${q(queryText)},`,
        "      limit: 5,",
        "    },",
        `    expected: { expectedIds: [${q(s.id)}] },`,
        "  },",
      ].join("\n");
    })
    .join("\n");

  return (
    header(
      'import type { SearchExpected, SearchInput } from "../fixtures/search.synthetic.js";\nimport type { SeedMemory } from "../fixtures/search.synthetic.js";',
    ) +
    `export const seedMemories: SeedMemory[] = [\n${seedBody}\n];\n\n` +
    `export const fixtures: FixtureCase<SearchInput, SearchExpected>[] = [\n${queryBody}\n];\n`
  );
}

function renderTimeUpdate(memories: YuiMemoryRow[]): string {
  const nowIso = new Date().toISOString();
  const candidates = memories
    .filter((m) => RELATIVE_TIME_RE.test(m.narrative))
    .slice(0, 50);

  const body = candidates
    .map((m, i) =>
      [
        "  {",
        `    id: "private-time-update-${String(i + 1).padStart(3, "0")}",`,
        `    description: ${q(`yui mem ${m.id.slice(0, 8)} (${m.kind})`)},`,
        "    input: {",
        `      narrative: ${q(m.narrative)},`,
        `      createdAtIso: ${q(m.created_at.toISOString())},`,
        `      nowIso: ${q(nowIso)},`,
        "    },",
        "    // TODO: human review — replace expected.narrative with the absolute-date rewrite.",
        `    expected: { narrative: ${q(m.narrative)}, minCosine: 0.85 },`,
        '    tags: ["ambiguous"],',
        "  },",
      ].join("\n"),
    )
    .join("\n");

  return (
    header(
      'import type { TimeUpdateExpected, TimeUpdateInput } from "../fixtures/time-update.synthetic.js";',
    ) +
    `export const fixtures: FixtureCase<TimeUpdateInput, TimeUpdateExpected>[] = [\n${body}\n];\n`
  );
}

// ---------------------------------------------------------------------------
// SQL extraction
// ---------------------------------------------------------------------------

async function loadMemories(pool: pg.Pool): Promise<YuiMemoryRow[]> {
  const r = await pool.query<YuiMemoryRow>(`
    SELECT id::text AS id, narrative, importance, kind, created_at
    FROM memories
    WHERE archived_at IS NULL
      AND narrative IS NOT NULL
      AND length(narrative) >= 20
    ORDER BY importance DESC NULLS LAST, created_at DESC
    LIMIT 300
  `);
  return r.rows;
}

async function loadPromoteCases(pool: pg.Pool): Promise<YuiPromoteRow[]> {
  const linkedRes = await pool.query<YuiPromoteRow>(`
    SELECT
      o.id::text AS obs_id, o.source, o.kind,
      o.payload->>'narrative' AS narrative,
      o.recorded_at,
      TRUE AS linked
    FROM observations o
    WHERE o.archived_at IS NULL
      AND o.summarized_at IS NOT NULL
      AND length(o.payload->>'narrative') >= 30
      AND EXISTS (
        SELECT 1 FROM memories m
        WHERE m.archived_at IS NULL
          AND o.id::text = ANY(m.source_observation_ids)
      )
    ORDER BY o.recorded_at DESC
    LIMIT 150
  `);
  const unlinkedRes = await pool.query<YuiPromoteRow>(`
    SELECT
      o.id::text AS obs_id, o.source, o.kind,
      o.payload->>'narrative' AS narrative,
      o.recorded_at,
      FALSE AS linked
    FROM observations o
    WHERE o.archived_at IS NULL
      AND o.summarized_at IS NOT NULL
      AND length(o.payload->>'narrative') >= 30
      AND NOT EXISTS (
        SELECT 1 FROM memories m
        WHERE m.archived_at IS NULL
          AND o.id::text = ANY(m.source_observation_ids)
      )
    ORDER BY o.recorded_at DESC
    LIMIT 150
  `);
  // Interleave linked/unlinked so the file is balanced and not in source order.
  const interleaved: YuiPromoteRow[] = [];
  const maxLen = Math.max(linkedRes.rows.length, unlinkedRes.rows.length);
  for (let i = 0; i < maxLen; i++) {
    if (i < linkedRes.rows.length) interleaved.push(linkedRes.rows[i]!);
    if (i < unlinkedRes.rows.length) interleaved.push(unlinkedRes.rows[i]!);
  }
  return interleaved;
}

async function loadAudnCases(pool: pg.Pool): Promise<YuiAudnRow[]> {
  // UPDATE and DELETE distributions in yui are very imbalanced (4328 vs 593).
  // Query them separately so DELETE has enough representation in the fixture.
  const updateRes = await pool.query<YuiAudnRow>(`
    SELECT id::text AS hist_id, memory_id::text AS memory_id, action,
           before_narrative, after_narrative, reason, applied_at
    FROM memory_history
    WHERE action = 'UPDATE'
      AND before_narrative IS NOT NULL AND length(before_narrative) >= 20
      AND (after_narrative IS NOT NULL OR reason IS NOT NULL)
    ORDER BY applied_at DESC
    LIMIT 150
  `);
  const deleteRes = await pool.query<YuiAudnRow>(`
    SELECT id::text AS hist_id, memory_id::text AS memory_id, action,
           before_narrative, after_narrative, reason, applied_at
    FROM memory_history
    WHERE action = 'DELETE'
      AND before_narrative IS NOT NULL AND length(before_narrative) >= 20
      AND (after_narrative IS NOT NULL OR reason IS NOT NULL)
    ORDER BY applied_at DESC
    LIMIT 100
  `);
  // Interleave so the fixture is balanced.
  const interleaved: YuiAudnRow[] = [];
  const maxLen = Math.max(updateRes.rows.length, deleteRes.rows.length);
  for (let i = 0; i < maxLen; i++) {
    if (i < updateRes.rows.length) interleaved.push(updateRes.rows[i]!);
    if (i < deleteRes.rows.length) interleaved.push(deleteRes.rows[i]!);
  }
  return interleaved;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

async function main(): Promise<void> {
  const databaseUrl = process.env["YUI_DATABASE_URL"];
  if (!databaseUrl) {
    throw new Error("YUI_DATABASE_URL is required");
  }

  const pool = new Pool({ connectionString: databaseUrl });

  try {
    const [memories, promoteRows, audnRows] = await Promise.all([
      loadMemories(pool),
      loadPromoteCases(pool),
      loadAudnCases(pool),
    ]);

    await mkdir(OUT_DIR, { recursive: true });

    await Promise.all([
      writeFile(
        join(OUT_DIR, "promote.private.ts"),
        renderPromote(promoteRows),
      ),
      writeFile(join(OUT_DIR, "audn.private.ts"), renderAudn(audnRows)),
      writeFile(join(OUT_DIR, "search.private.ts"), renderSearch(memories)),
      writeFile(
        join(OUT_DIR, "time-update.private.ts"),
        renderTimeUpdate(memories),
      ),
    ]);

    const linkedCount = promoteRows.filter((r) => r.linked).length;
    const updateCount = audnRows.filter((r) => r.action === "UPDATE").length;
    const deleteCount = audnRows.filter((r) => r.action === "DELETE").length;
    const timeUpdateCount = memories.filter((m) =>
      RELATIVE_TIME_RE.test(m.narrative),
    ).length;

    console.log(
      [
        "wrote eval private fixtures:",
        `  memories sampled:    ${memories.length}`,
        `  promote cases:       ${promoteRows.length} (keep ${linkedCount} / skip ${promoteRows.length - linkedCount})`,
        `  audn cases:          ${audnRows.length} (UPDATE ${updateCount} / DELETE ${deleteCount})`,
        `  search:              ${Math.min(100, memories.length)} seed memories + ${Math.min(80, memories.length)} self-recall queries`,
        `  time-update cases:   ${Math.min(50, timeUpdateCount)} (TODO labels)`,
        `  outDir:              ${OUT_DIR}`,
      ].join("\n"),
    );
  } finally {
    await pool.end();
  }
}

main().catch((err) => {
  console.error(err instanceof Error ? err.message : err);
  process.exit(1);
});
