async function main(): Promise<void> {
  const databaseUrl = process.env["YUI_DATABASE_URL"];
  if (!databaseUrl) {
    throw new Error("YUI_DATABASE_URL is required");
  }

  const pool = new Pool({ connectionString: databaseUrl });

  try {
    const [memoryResult, observationResult, decisionResult, linkResult] =
      await Promise.all([
        pool.query<YuiMemory>(
          `SELECT id, narrative, importance, kind, created_at
           FROM memories
           WHERE archived_at IS NULL
           ORDER BY importance DESC, created_at DESC
           LIMIT 300`,
        ),
        pool.query<YuiObservation>(
          `SELECT id, content, type, source, created_at
           FROM observations
           ORDER BY created_at DESC
           LIMIT 500`,
        ),
        pool.query<YuiDecision>(
          `SELECT id, content, status, created_at
           FROM decisions
           ORDER BY CASE WHEN status = 'in_progress' THEN 0 ELSE 1 END, created_at DESC
           LIMIT 150`,
        ),
        pool.query<YuiLink>(
          `SELECT from_id, to_id, relation
           FROM links
           LIMIT 500`,
        ),
      ]);

    const memories = memoryResult.rows;
    const observations = observationResult.rows;
    const decisions = decisionResult.rows;
    const links = linkResult.rows;

    await mkdir(OUT_DIR, { recursive: true });
    await Promise.all([
      writeFile(join(OUT_DIR, "audn.private.ts"), renderAudn(observations)),
      writeFile(
        join(OUT_DIR, "promote.private.ts"),
        renderPromote(observations),
      ),
      writeFile(
        join(OUT_DIR, "search.private.ts"),
        renderSearch(memories, observations),
      ),
      writeFile(
        join(OUT_DIR, "contradiction.private.ts"),
        renderContradiction(decisions),
      ),
      writeFile(
        join(OUT_DIR, "time-update.private.ts"),
        renderTimeUpdate(memories),
      ),
    ]);

    console.log(
      [
        "wrote eval private fixtures:",
        `  memories: ${memories.length}`,
        `  observations: ${observations.length}`,
        `  decisions: ${decisions.length}`,
        `  links: ${links.length}`,
        `  outDir: ${OUT_DIR}`,
      ].join("\n"),
    );
  } finally {
    await pool.end();
  }
}

main().catch((err) => {
  console.error(err instanceof Error ? err.message : err);
  process.exit(1);
});
